
# Vm算法介绍
# 每个节点与前后两个节点构成三角形
# 删除面积最小的三角形的顶点
# 重新构成新的三角形面积
# 再次删除最小面积
# 直到最后余下首尾两点

# 先删除的点 对图形的形状影响不大 后删除的点 对图形的形状影响高
# 重要性排序 后删除最重要

'''
算法思路：
    将所有的点存放在数组中，【1、2、3、4、5、...、n】
    新建两个数组
    A：一个用来存放删除的点，
    B:一个用来存放优化结果（未删除的点）

    先将首点，放入A数组中，
    相邻三点计算面积【记录面积、中间点（三角顶点）的index】、
    进行比较，将最小面积所在的顶点 存放至A数组，同时将该点从原数组中删除
    迭代，
    1、直至留下首尾两点 2、或者留下不可删除的所有点
    若只留下首尾两点，那么将尾点放入A数组，A数组的排序几位 【首点-》先删除-》后删除-》尾点】
    若留下点数大于2，B数组存放即为保留点，将剩下的点，再放入A中，
    此时，A数组排序【首点-》先删除-》保留点（包括尾点）】
'''

# 三点 求三角形面积 “向量法”
def points2triangleArea(vertex,pointStr,pointEnd):
    (x1, y1), (x2, y2), (x3, y3) = vertex,pointStr,pointEnd
    return 0.5 * abs(x2 * y3 + x1 * y2 + x3 * y1 - x3 * y2 - x2 * y1 - x1 * y3)

# 计算最小面积 并返回索引（目标顶点）
def findMinarea(Points):
    index = 0 # 最小面积的顶点索引
    minAreaRect = 0 # 最小面积
    minArea = [] # 构建数组 [index,minAreaValue]
    # 若只存在两个点 则退出
    if len(Points) <= 2:
        return 0
    for i in range(1,len(Points)-1):
        Area = points2triangleArea(Points[i],Points[i-1],Points[i+1])
        # 解决数组中三点共线 面积为零的情况
        if minAreaRect == 0 and index == 0:
            minAreaRect = Area
            index = i
            continue
        # 解决多个面积相同 且均小于阈值的情况
        if Area == minAreaRect:
            minAreaRect = Area
            index = i
            continue
        if Area < minAreaRect:
            minAreaRect = Area
            index = i
    minArea.append(index)
    minArea.append(minAreaRect)
    return minArea

# Visvalingam-Whyatt 算法 非递归
# 先加入了 【尾点】【不重要】【重要】【首点】
# 最终结果:颠倒顺序 【首点】【重要】【不重要】【尾点】
def VM_nonrecursive(Points,threshold):
    # VMFindout 保留的是点的重要性排序 【尾点，先删除，后删除，‘需保留’，首点】
    VMFindout = []
    # VMsimplification 保留的是化简后的结果
    VMSimplification = []
    # 如果 坐标点的数量小于等于2
    if len(Points) <= 2:
        VMFindout = Points
        return VMFindout
    # 先将尾点加入数组中
    VMFindout.append(Points[-1])
    while len(Points) > 2 :
        minAreaRectIndex = findMinarea(Points)[0]
        minArea = findMinarea(Points)[1]
        # 如果最小面积小于阈值
        if minArea < threshold:
            # 将该点添加至数组中
            VMFindout.append(Points[minAreaRectIndex])
            # 将该点从数组中删除
            # 先删除的点 对图形的形变影响最小
            Points.remove(Points[minAreaRectIndex])
        else:
            # 当所有的点 构成的三角形的面积 均大于阈值时
            # 接下来的点不可删去
            break
    for i in range(len(Points)):
        VMSimplification.append(Points[i])
    if len(Points) == 2:
        # 最后将尾点加入数组中
        VMFindout.append(Points[0])
    elif len(Points) > 2:
        # print(VMSimplification)
        Points.remove(Points[0])
        for i in range(len(Points)):
            VMFindout.append(Points[i])
    # 顺序颠倒 最重要的点 放前面【同时，首尾也交换】
    return VMFindout[::-1]


